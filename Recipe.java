package com.model;

public class Recipe {
	private int num;
	private String cookname;
	private String cookkind;
	private String cookstuff;
	private String cookrecipe;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getCookname() {
		return cookname;
	}
	public void setCookname(String cookname) {
		this.cookname = cookname;
	}
	public String getCookkind() {
		return cookkind;
	}
	public void setCookkind(String cookkind) {
		this.cookkind = cookkind;
	}
	public String getCookstuff() {
		return cookstuff;
	}
	public void setCookstuff(String cookstuff) {
		this.cookstuff = cookstuff;
	}
	public String getCookrecipe() {
		return cookrecipe;
	}
	public void setCookrecipe(String cookrecipe) {
		this.cookrecipe = cookrecipe;
	}
}
